public class SearchCar {
    private String carModelMara;
    private String modelId;
    private String carCategoryId;

    public String getCarModelMara() {
        return carModelMara;
    }

    public void setCarModelMara(String carModelMara) {
        this.carModelMara = carModelMara;
    }

    public String getModelId() {
        return modelId;
    }

    public void setModelId(String modelId) {
        this.modelId = modelId;
    }

    public String getCarCategoryId() {
        return carCategoryId;
    }

    public void setCarCategoryId(String carCategoryId) {
        this.carCategoryId = carCategoryId;
    }
}
